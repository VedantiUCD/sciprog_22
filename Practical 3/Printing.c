# include <stdio.h>

int main()
{
    int int1= 20;
    int int2= 24;
    float float1 = 133.7657568;
    float float2 = 12287189.89389389;
    
// printing default numbers formats
    printf("Two integer numbers are  %d %d \n", int1, int2);
    printf("Two floating numbers %f %f\n",float1, float2);


// printing floats with 2 decimal places
printf("\n\n Floating numbers  2 decimal places %.2f %.2f\t ", float1, float2);

}
